export interface Youth {
  id: string;
  firstName: string;
  lastName: string;
  email: string;
  phone: string;
  dateOfBirth: string;
  gender: 'male' | 'female';
  avatar?: string;
  address: string;
  educationStatus: 'high_school' | 'college' | 'working' | 'unemployed';
  occupation?: string;
  joinDate: string;
  status: 'active' | 'inactive';
  engagementScore: number;
  engagementStatus: 'engaged' | 'at-risk' | 'disengaged';
  smallGroup?: string;
  mentor?: string;
  leadershipLevel: 'none' | 'emerging' | 'developing' | 'established';
  discipleshipStatus: 'new_member' | 'developing' | 'experienced' | 'leader';
  attendanceRate: number;
  lastAttendance?: string;
  notes?: string;
  ministryAreas: string[];
  ageGroup: 'under-18' | '18-30' | '31-45' | '46-60' | '60+';
}

export interface Program {
  id: string;
  name: string;
  description: string;
  category: 'gathering' | 'development' | 'outreach' | 'fellowship' | 'leadership' | 'learning';
  startDate: string;
  endDate?: string;
  isActive: boolean;
  participantCount: number;
  maxCapacity?: number;
  leader: string;
  schedule: string;
  scheduleType: 'weekend' | 'weekday' | 'special';
  averageAttendance: number;
  engagementScore: number;
  memberBreakdown: {
    students: number;
    employed: number;
    unemployed: number;
  };
}

export interface AttendanceRecord {
  id: string;
  youthId: string;
  programId: string;
  date: string;
  present: boolean;
}

export interface SmallGroup {
  id: string;
  name: string;
  leader: string;
  memberCount: number;
  meetingDay: string;
  meetingTime: string;
  location: string;
}

